class Operatorss():

    def __init__(self, a, b):
        self.a = a
        self.b = b
        print(self.a, self.b)

    def add(self, a, b):
        return self.a + self.b

    def sub(self, a, b):
        return self.a - self.b

    def mul(self, a, b):
        return self.a * self.b

    def div(self, a, b):
        return self.a / self.b


        